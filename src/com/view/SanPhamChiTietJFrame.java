    private void showDetail(int index) {
        SanPhamChiTiet spct = service.getSPCT().get(index);

        txtMa.setText(spct.getMaSP());
        txtTen.setText(service.getTenSP(spct.getMaSP()));
        txtGia.setText(spct.getGia() + "");
        txtSoLuong.setText(spct.getSoLuong() + "");
        rdoHangCu.setSelected(spct.getTrangThai().equals("Hàng cũ"));
        rdoHangMoi.setSelected(spct.getTrangThai().equals("Hàng mới"));
        cboMau.setSelectedItem(service.getTenMau(spct.getMaMS() + ""));
        cboSize.setSelectedItem(service.getTenSize(spct.getMaSize() + ""));
        cbochatLieu.setSelectedItem(service.getTenCL(spct.getMaCL() + ""));
    }

    private SanPhamChiTiet readForm() {
        SanPhamChiTiet spct = new SanPhamChiTiet();
        spct.setMaSP(txtMa.getText());
        spct.setMaMS(service.getMaMau(cboMau.getSelectedItem() + ""));
        spct.setMaCL(service.getMaCL(cbochatLieu.getSelectedItem() + ""));
        spct.setMaSize(service.getMaSize(cboSize.getSelectedItem() + ""));
        spct.setSoLuong(Integer.parseInt(txtSoLuong.getText()));
        spct.setGia(Double.parseDouble(txtGia.getText()));
        spct.setMoTa(tatMoTa.getText());
        if (rdoHangCu.isSelected()) {
            spct.setTrangThai("Hàng cũ");
        } else {
            spct.setTrangThai("Hàng mới");
        }

        return spct;
    }
